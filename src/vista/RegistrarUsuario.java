    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        nom=txtnom.getText();
        correo=txtcorreo.getText();
        tele=txttel.getText();
        username=txtuser.getText();
        pass=txtcontra.getText();
        if(!nom.equals("") && !correo.equals("") && !tele.equals("") && !username.equals("") && !pass.equals("")){
        
        registrarusuarios.registrarUsuarios(nom, correo, tele, username, pass, combopermiso, nombre);
        
                txtnom.setText("");
                txtcorreo.setText("");
                txttel.setText("");
                txtuser.setText("");
                txtcontra.setText("");

                txtnom.setBackground(Color.GREEN);
                txtcorreo.setBackground(Color.GREEN);
                txttel.setBackground(Color.GREEN);
                txtuser.setBackground(Color.GREEN);
                txtcontra.setBackground(Color.GREEN);
                JOptionPane.showMessageDialog(null, "usuario registrado ");
                 txtnom.setBackground(Color.WHITE);
                txtcorreo.setBackground(Color.WHITE);
                txttel.setBackground(Color.WHITE);
                txtuser.setBackground(Color.WHITE);
                txtcontra.setBackground(Color.WHITE);
                
        }else{
             JOptionPane.showMessageDialog(null, "debe de llanar todos los campos");
            if(nom.equals("")){
                txtnom.setBackground(Color.red);
            }else{
                txtnom.setBackground(Color.WHITE);
            }
            if(correo.equals("")){
                txtcorreo.setBackground(Color.red);
            }else{
                txtcorreo.setBackground(Color.WHITE);
            }
            if(tele.equals("")){
                txttel.setBackground(Color.red);
            }else{
                txttel.setBackground(Color.WHITE);
            }
            if(username.equals("")){
                txtuser.setBackground(Color.red);
            }else{
                txtuser.setBackground(Color.WHITE);
            }
            if(pass.equals("")){
                txtcontra.setBackground(Color.red);
            }else{
                txtcontra.setBackground(Color.WHITE);
            }
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

