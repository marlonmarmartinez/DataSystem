    private void btnregistrarclienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarclienteActionPerformed
        
        if(!txtnomcli1.getText().equals("") && !txtcorreocli.getText().equals("") && !txttelcli.getText().equals("") && !txtdireccioncli.getText().equals("")){
             registrarcliente.registrarClientes(txtnomcli1, txtcorreocli, txttelcli, txtdireccioncli, username);
             txtnomcli1.setText("");
             txtcorreocli.setText("");
             txttelcli.setText("");
             txtdireccioncli.setText("");
             
            txtnomcli1.setBackground(Color.GREEN);
            txtcorreocli.setBackground(Color.GREEN);
            txttelcli.setBackground(Color.GREEN);
            txtdireccioncli.setBackground(Color.GREEN);
           
            JOptionPane.showMessageDialog(null, "usuario registrado ");
            txtnomcli1.setBackground(Color.white);
            txtcorreocli.setBackground(Color.white);
            txttelcli.setBackground(Color.white);
            txtdireccioncli.setBackground(Color.white);
        }else{
            JOptionPane.showMessageDialog(null, "debe de llanar todos los campos");
             if(txtnomcli1.getText().equals("")){
                 txtnomcli1.setBackground(Color.red);
             }else{
                 txtnomcli1.setBackground(Color.WHITE);
             }
             if(txtcorreocli.getText().equals("")){
                 txtcorreocli.setBackground(Color.red);
             }else{
                 txtcorreocli.setBackground(Color.WHITE);
             }
             if(txttelcli.getText().equals("")){
                 txttelcli.setBackground(Color.red);
             }else{
                 txttelcli.setBackground(Color.WHITE);
             }
             if(txtdireccioncli.getText().equals("")){
                 txtdireccioncli.setBackground(Color.red);
             }else{
                 txtdireccioncli.setBackground(Color.WHITE);
             }
        }
        
    }//GEN-LAST:event_btnregistrarclienteActionPerformed

